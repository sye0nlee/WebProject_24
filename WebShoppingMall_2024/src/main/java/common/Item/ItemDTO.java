package common.Item;

public class ItemDTO{

	String item_id;
	String item_name;
	String item_seller;
	String item_info;
	Integer item_price;
	Integer item_left;
	Integer item_like;
	Integer item_view;
	String info_pwd;


	public String getItem_id() {
		return item_id;
	}
	public void setItem_id(String item_id) {
		this.item_id = item_id;
	}
	public String getItem_name() {
		return item_name;
	}
	public void setItem_name(String item_name) {
		this.item_name = item_name;
	}
	public String getItem_seller() {
		return item_seller;
	}
	public void setItem_seller(String item_seller) {
		this.item_seller = item_seller;
	}
	public String getItem_info() {
		return item_info;
	}
	public void setItem_info(String item_info) {
		this.item_info = item_info;
	}
	public Integer getItem_price() {
		return item_price;
	}
	public void setItem_price(Integer item_price) {
		this.item_price = item_price;
	}
	public Integer getItem_left() {
		return item_left;
	}
	public void setItem_left(Integer item_left) {
		this.item_left = item_left;
	}
	public Integer getItem_like() {
		return item_like;
	}
	public void setItem_like(Integer item_like) {
		this.item_like = item_like;
	}
	public Integer getItem_view() {
		return item_view;
	}
	public void setItem_view(Integer item_view) {
		this.item_view = item_view;
	}
	public String getInfo_pwd() {
		return info_pwd;
	}
	public void setInfo_pwd(String info_pwd) {
		this.info_pwd = info_pwd;
	}

}
